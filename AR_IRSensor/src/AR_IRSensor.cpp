#include "AR_IRSensor.h"

/*
*********************************************************************************************************
*	函 数 名: AR_IRSensor()构造函数
*	功能说明:  
*	形    参: p: port to IR sensor
*	返 回 值: 无
*********************************************************************************************************
*/
AR_IRSensor::AR_IRSensor(uint8_t p)
{
	pin = p;
	pinMode(pin, INPUT_PULLUP);
}
/*
*********************************************************************************************************
*	函 数 名: getStatu
*	功能说明: get IR sensor statu

*	形    参: 无
*	返 回 值: 0:无信号
*			  1：有信号
*********************************************************************************************************
*/
uint8_t AR_IRSensor::getStatu()
{
	pinMode(pin, INPUT_PULLUP);
	if( digitalRead(pin) == HIGH )
		return 0;
	else
		return 1;
}
